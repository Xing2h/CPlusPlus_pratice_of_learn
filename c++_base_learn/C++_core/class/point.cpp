#include"point.h"

//设置点的横坐标
void Point::setM_X(int x) {
    m_X = x;
}

//获取点的横坐标
int Point::getM_X() {
    return m_X;
}

//设置点的纵坐标
void Point::setM_Y(int y) {
    m_Y = y;
}

//获取点的纵坐标
int Point::getM_Y() {
    return m_Y;
}

